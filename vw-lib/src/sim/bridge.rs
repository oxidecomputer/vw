/// Generate `src/generated.rs` using `quote` and `prettyplease`.
fn write_generated_rs(src_dir: &Path, mist: &MistConfig) -> crate::Result<()> {
    let clock_hz = Literal::f64_unsuffixed(mist.clock);
    let clock_period_val = 1.0 / mist.clock;
    let clock_period = Literal::f64_unsuffixed(clock_period_val);
    let prime_cycles = mist.prime_cycles.unwrap_or(0);

    let netlist_path = &mist.netlist;

    // Build DAC constants
    let mut dac_const_defs = Vec::new();
    let mut port_names_sorted: Vec<&String> = mist.ports.keys().collect();
    port_names_sorted.sort();

    for port_name in &port_names_sorted {
        let mapping = &mist.ports[port_name.as_str()];
        let const_name = format_ident!("{}", mapping.dac.to_uppercase());
        let dac_value = format!("YDAC!{}", mapping.dac.to_uppercase());
        let dac_lit = Literal::string(&dac_value);
        dac_const_defs.push(quote! {
            pub const #const_name: &str = #dac_lit;
        });
    }

    // Build Signals struct fields and constructor
    let mut struct_fields = Vec::new();
    let mut ctor_fields = Vec::new();

    for port_name in &port_names_sorted {
        let field_ident = format_ident!("{}", port_name);
        let port_lit = Literal::string(port_name);
        struct_fields.push(quote! {
            pub #field_ident: SimHandle
        });
        ctor_fields.push(quote! {
            #field_ident: dut.get_child(#port_lit)?
        });
    }

    // Build update_dacs body
    let mut update_stmts = Vec::new();
    for port_name in &port_names_sorted {
        let mapping = &mist.ports[port_name.as_str()];
        let field_ident = format_ident!("{}", port_name);
        let dac_const = format_ident!("{}", mapping.dac.to_uppercase());
        let convert_fn = match mapping.encoding.as_str() {
            "pam4" => format_ident!("binstr_to_pam4"),
            _ => format_ident!("binstr_to_unsigned"),
        };

        update_stmts.push(quote! {
            xyce.update_dac(
                #dac_const,
                &[t],
                &[#convert_fn(&sigs.#field_ident.get_value_binstr().unwrap_or_default())],
            )?;
        });
    }

    let tokens = quote! {
        //! Auto-generated from mist.toml — do not edit.
        //! Re-generate with: `vw test <name> --scaffold`

        #![allow(dead_code)]

        use rust_cosim::handles::SimHandle;
        use rust_cosim::TestError;
        use crate::xyce::Xyce;

        /// Clock frequency in Hz.
        pub const CLOCK_HZ: f64 = #clock_hz;

        /// Clock period in seconds.
        pub const CLOCK_PERIOD: f64 = #clock_period;

        /// Number of pipeline priming cycles.
        pub const PRIME_CYCLES: u32 = #prime_cycles;

        /// Path to the Xyce netlist, relative to the crate root.
        pub const NETLIST: &str = concat!(env!("CARGO_MANIFEST_DIR"), "/", #netlist_path);

        // DAC device names (fully-qualified as they appear in the Xyce netlist)
        #(#dac_const_defs)*

        /// Signal handles for all mapped ports.
        pub struct Signals {
            #(#struct_fields),*
        }

        impl Signals {
            /// Acquire signal handles from the top-level DUT.
            pub fn new(dut: &SimHandle) -> Result<Self, TestError> {
                Ok(Self {
                    #(#ctor_fields),*
                })
            }
        }

        /// Convert a 2-bit PAM4 binary string to a symbol voltage (0.0–3.0).
        pub fn binstr_to_pam4(binstr: &str) -> f64 {
            match binstr {
                "00" => 0.0,
                "01" => 1.0,
                "10" => 2.0,
                "11" => 3.0,
                _ => {
                    eprintln!("warning: unexpected PAM4 binstr '{binstr}', defaulting to 0");
                    0.0
                }
            }
        }

        /// Convert an N-bit binary string to an unsigned integer as f64.
        pub fn binstr_to_unsigned(binstr: &str) -> f64 {
            u64::from_str_radix(binstr, 2).unwrap_or(0) as f64
        }

        /// Read all mapped VHDL ports and update the corresponding Xyce DACs.
        pub fn update_dacs(sigs: &Signals, xyce: &mut Xyce, t: f64) -> Result<(), String> {
            #(#update_stmts)*
            Ok(())
        }
    };

    let file =
        syn::parse2::<syn::File>(tokens).map_err(|e| VwError::CodeGen {
            message: format!("Failed to parse generated code: {e}"),
        })?;
    let formatted = prettyplease::unparse(&file);

    write_file(src_dir.join("generated.rs"), &formatted)
}

/// Generate the template `src/lib.rs` (only written if file doesn't exist).
fn write_template_lib_rs(
    src_dir: &Path,
    mist: &MistConfig,
) -> crate::Result<()> {
    // Half-period in picoseconds, rounded to nearest integer
    let half_period_ps = (0.5e12 / mist.clock).round() as u64;
    let half_period_ps_lit = Literal::u64_unsuffixed(half_period_ps);

    // Build DAC initialization statements
    let mut init_stmts = Vec::new();
    let mut port_names_sorted: Vec<&String> = mist.ports.keys().collect();
    port_names_sorted.sort();

    for port_name in &port_names_sorted {
        let mapping = &mist.ports[port_name.as_str()];
        let dac_const = format_ident!("{}", mapping.dac.to_uppercase());
        init_stmts.push(quote! {
            xyce.update_dac(#dac_const, &[0.0], &[0.0]).map_err(TestError::IoError)?;
        });
    }

    let tokens = quote! {
        //! Mixed-signal testbench.
        //!
        //! This file was scaffolded by `vw test --scaffold` and is yours to edit.
        //! The `generated` module provides DAC constants, signal handles, encoding
        //! helpers, and the `update_dacs()` bulk-update function.

        mod xyce;
        mod generated;

        use rust_cosim::clock::Clock;
        use rust_cosim::executor;
        use rust_cosim::handles::{SetAction, SimHandle};
        use rust_cosim::triggers::{Edge, ReadWrite};
        use rust_cosim::utils::ps;
        use rust_cosim::TestError;
        use std::future::Future;
        use std::path::PathBuf;
        use std::pin::Pin;

        use xyce::Xyce;
        use generated::*;

        #[no_mangle]
        pub fn testbench_main(
            dut: SimHandle,
        ) -> Pin<Box<dyn Future<Output = Result<(), TestError>>>> {
            Box::pin(async move {
                println!("mixed-signal testbench started");

                // Mapped port handles (DAC outputs)
                let sigs = Signals::new(&dut)?;

                // Clock and other input handles (not in port mapping)
                let clk = dut.get_child("clk")?;

                // Initialize Xyce
                let netlist = PathBuf::from(NETLIST);
                let mut xyce = Xyce::new(&netlist).map_err(TestError::IoError)?;

                // Initialize all DACs to zero
                #(#init_stmts)*

                // Start the clock
                executor::spawn(Clock::new(clk, ps(#half_period_ps_lit)).run());

                // Let the simulator propagate initial values
                ReadWrite::new().await;

                // Step Xyce past t=0
                xyce.simulate_until(1e-12).map_err(TestError::IoError)?;

                let mut sim_time: f64 = 0.0;

                // ---- Prime the pipeline ----
                for _ in 0..PRIME_CYCLES {
                    Edge::rising(clk).await;
                    sim_time += CLOCK_PERIOD;
                    update_dacs(&sigs, &mut xyce, sim_time).map_err(TestError::IoError)?;
                    xyce.simulate_until(sim_time + CLOCK_PERIOD * 0.9)
                        .map_err(TestError::IoError)?;
                }

                // ---- Main test logic (program your test here) ----
                for cycle in 0..16u32 {
                    Edge::rising(clk).await;
                    sim_time += CLOCK_PERIOD;

                    update_dacs(&sigs, &mut xyce, sim_time).map_err(TestError::IoError)?;
                    xyce.simulate_until(sim_time + CLOCK_PERIOD * 0.9)
                        .map_err(TestError::IoError)?;

                    let v_p = xyce.get_circuit_value("V(OUT_P)");
                    let v_n = xyce.get_circuit_value("V(OUT_N)");
                    println!(
                        "cycle={cycle:3} V(OUT_P)={v_p:+.4} V(OUT_N)={v_n:+.4} V(diff)={:+.4}",
                        v_p - v_n,
                    );
                }

                println!("mixed-signal testbench complete");
                Ok(())
            })
        }
    };

    let file =
        syn::parse2::<syn::File>(tokens).map_err(|e| VwError::CodeGen {
            message: format!("Failed to parse generated template: {e}"),
        })?;
    let formatted = prettyplease::unparse(&file);

    write_file(src_dir.join("lib.rs"), &formatted)
}
