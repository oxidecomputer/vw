//! Scaffold generator for mixed-signal bridge crates.
//!
//! Generates a complete Rust crate in the bench directory that bridges
//! NVC (VHDL simulator) and Xyce (SPICE simulator) via VHPI.

use std::path::Path;
use std::{fs, io::Write};

use proc_macro2::Literal;
use quote::{format_ident, quote};

use crate::{MistConfig, ToolsConfig, VwError};

/// The Xyce FFI wrapper (verbatim from the mist prototype).
const XYCE_RS: &str = include_str!("bridge_assets/xyce.rs");

/// The C++ shim implementing the xyce_* C interface.
const XYCE_CINTERFACE_CPP: &str =
    include_str!("bridge_assets/xyce_cinterface.cpp");

/// Generate or regenerate scaffold files in a bench directory.
///
/// Files that are always regenerated (derived from mist.toml):
/// - `Cargo.toml`, `build.rs`, `xyce_cinterface.cpp`
/// - `src/xyce.rs`, `src/generated.rs`
///
/// Files created only if missing (user-owned):
/// - `src/lib.rs`
pub fn generate_scaffold(
    bench_dir: &Path,
    mist_config: &MistConfig,
    tools: &Option<ToolsConfig>,
) -> crate::Result<()> {
    let src_dir = bench_dir.join("src");
    fs::create_dir_all(&src_dir)?;

    let bench_name = bench_dir
        .file_name()
        .unwrap_or_default()
        .to_string_lossy()
        .to_string();

    // Resolve tool paths with defaults
    let xyce_prefix = tools
        .as_ref()
        .and_then(|t| t.xyce.as_ref())
        .map(|x| expand_tilde(&x.prefix))
        .unwrap_or_else(|| {
            let home = std::env::var("HOME").unwrap_or_default();
            format!("{home}/.local/xyce")
        });

    let trilinos_prefix = tools
        .as_ref()
        .and_then(|t| t.xyce.as_ref())
        .map(|x| expand_tilde(&x.trilinos_prefix))
        .unwrap_or_else(|| {
            let home = std::env::var("HOME").unwrap_or_default();
            format!("{home}/.local/trilinos")
        });

    let rust_cosim_path = tools
        .as_ref()
        .and_then(|t| t.rust_cosim.as_ref())
        .map(|r| expand_tilde(&r.path))
        .unwrap_or_else(|| {
            let home = std::env::var("HOME").unwrap_or_default();
            format!("{home}/src/rust-cosim")
        });

    // Always regenerated files
    write_cargo_toml(bench_dir, &bench_name, &rust_cosim_path)?;
    write_build_rs(bench_dir, &xyce_prefix, &trilinos_prefix)?;
    write_file(bench_dir.join("xyce_cinterface.cpp"), XYCE_CINTERFACE_CPP)?;
    write_file(src_dir.join("xyce.rs"), XYCE_RS)?;
    write_generated_rs(&src_dir, mist_config)?;

    // User-owned files (only if missing)
    let lib_rs = src_dir.join("lib.rs");
    if !lib_rs.exists() {
        write_template_lib_rs(&src_dir, mist_config)?;
    }

    write_gitignore(bench_dir)?;

    Ok(())
}

fn write_gitignore(bench_dir: &Path) -> crate::Result<()> {
    let content = "\
# Auto-generated by `vw test --scaffold` — safe to regenerate.
# These files are derived from mist.toml and [tools] config.
Cargo.toml
build.rs
xyce_cinterface.cpp
src/xyce.rs
src/generated.rs

# Build artifacts
/target/
/output/
*.prn
";
    write_file(bench_dir.join(".gitignore"), content)
}

fn expand_tilde(path: &str) -> String {
    if let Some(rest) = path.strip_prefix("~/") {
        let home = std::env::var("HOME").unwrap_or_default();
        format!("{home}/{rest}")
    } else {
        path.to_string()
    }
}

fn write_file(path: impl AsRef<Path>, content: &str) -> crate::Result<()> {
    let mut f = fs::File::create(path.as_ref())?;
    f.write_all(content.as_bytes())?;
    Ok(())
}

fn write_cargo_toml(
    bench_dir: &Path,
    name: &str,
    rust_cosim_path: &str,
) -> crate::Result<()> {
    let content = format!(
        r#"# Auto-generated by `vw test --scaffold` — safe to regenerate.
[package]
name = "{name}"
version = "0.1.0"
edition = "2021"

[lib]
crate-type = ["cdylib"]

[build-dependencies]
cc = "1.0"

[dependencies]
rust-cosim = {{ path = "{rust_cosim_path}" }}
"#
    );
    write_file(bench_dir.join("Cargo.toml"), &content)
}

